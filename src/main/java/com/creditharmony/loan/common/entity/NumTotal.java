package com.creditharmony.loan.common.entity;

/**
 * 总金额和总条数
 * 
 * @Class Name NumberMaster
 * @author 翁私
 * @Create In 2016年3月26日
 */
public class NumTotal {
	
	private String num;
	
	private String total;
	
	private String reallyTotal;

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public String getTotal() {
		return total;
	}

	public void setTotal(String total) {
		this.total = total;
	}

	public String getReallyTotal() {
		return reallyTotal;
	}

	public void setReallyTotal(String reallyTotal) {
		this.reallyTotal = reallyTotal;
	}
	
	

}
